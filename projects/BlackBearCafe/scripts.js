function openSlideMenu() {
    document.getElementById('side-menu').style.width = '250px';
    document.getElementsById('side-menu').style.boxShadow = '-36px 0px 24px 10px rgba(46, 39, 34, .84)';
}

function closeSideMenu() {
    document.getElementById('side-menu').style.width = '0px';
    document.getElementsById('side-menu').style.boxShadow = '-149px 0px 24px 10px rgba(46, 39, 34, 0)';
}
